package hw3.chess;

/**
 * Represents the color of a chess piece.
 * Chess pieces can be either white or black, which determines their movement
 * and positioning at the start of the game.
 * 
 * @author Panagiotis Tsembekis
 * @version 1.0
 * @since 30/03/24
 */
public enum Color {
	/**
	 * White for white chess pieces
	 */
	WHITE,
	/**
	 * Black for black chess pices
	 */
	BLACK
}
